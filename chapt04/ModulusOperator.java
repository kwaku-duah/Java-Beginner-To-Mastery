package chapt04;

public class ModulusOperator {
    /* modulus operator for java, returns the remainder for an operation after
     * division
     */
    public static void main(String[] args) {
        int x = 42;
        double y = 42.25;

        System.out.println("the modulo for the int division is = " + x % 10);
        System.out.println("the modulo for the double division is = " + y % 10);
    }
}
