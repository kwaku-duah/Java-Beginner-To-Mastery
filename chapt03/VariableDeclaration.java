package chapt03;

public class VariableDeclaration {
    /* Variable declaration demonstrated in java 
     * follows TYPE IDENTIFIER (variable name) followed by equal then value
    */
    public static void main(String[] args){
        //declares multiple variables on one line
        int a = 10, b =10, c = 10;
        int e = 10, f = 12, g = 15;
        boolean d = true;
        double pi = 3.1416;
        System.out.println("The values are " + a + " " + b + " " + c + " " + e + " " + f +" " + g +" "+ pi + " "+ d);
    }

}
