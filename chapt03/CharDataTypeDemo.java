package chapt03;

public class CharDataTypeDemo {
    /* char datatypes can be used as int on which arithmetic operations can be
     * performed. 
     * You can add two characters together or increase their value
     */
    public static void main(String[] args) {
        char ch1;

        ch1 = 88;
        System.out.println("ch1 contains " + ch1);
        ch1++;
        System.out.println("ch1 now contains " + ch1);
    }
}
