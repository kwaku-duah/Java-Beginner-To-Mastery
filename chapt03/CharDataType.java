package chapt03;

public class CharDataType {
    public static void main(String[] args) {
        /* Usage of the char datatype demonstrated
         * use only single quotes, they are unicode characters
         * they are used to store a single character
         */
        
         char ch1, ch2;

         ch1 = 88;
         ch2 = 'Y';
         System.out.print("ch1 and ch2: ");
         System.out.println(ch1 + " " + ch2);
    }
}
